package org.jminor.swing.common.ui.dialog;

/**
 * Specifies if a dialog should be closed on Esc key press.
 */
public enum DisposeOnEscape {
  /**
   * Dispose on esc.
   */
  YES,
  /**
   * Do not dispose on esc.
   */
  NO
}

create schema scott;

CREATE TABLE scott.dept (
  deptno INT NOT NULL,
  dname VARCHAR(14) NOT NULL,
  loc VARCHAR(13),
  constraint dept_pk primary key (deptno),
  constraint dept_uk unique (dname)
);

CREATE TABLE scott.emp (
  empno INT NOT NULL,
  ename VARCHAR(10) NOT NULL,
  job VARCHAR(9),
  mgr INT,
  hiredate DATE,
  hiretime TIMESTAMP WITH TIME ZONE,
  sal DECIMAL(7, 2) NOT NULL,
  comm DECIMAL(7, 2) DEFAULT 200,
  deptno INT NOT NULL,
  data_lazy blob,
  data blob,
  constraint emp_pk primary key (empno),
  constraint emp_dept_fk foreign key (deptno) references scott.dept(deptno),
  constraint emp_mgr_fk foreign key (mgr) references scott.emp(empno)
);

CREATE SEQUENCE scott.emp_seq START WITH 17;

